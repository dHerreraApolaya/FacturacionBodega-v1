    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if(btnAgregar.getText().equalsIgnoreCase("AGREGAR")){
          //  mensaje("Ingreso a AGREGAR");
            limpiar();
            txtCodProducto.setText(""+(oProductoDao.tamaño()+1));
            desBlqTxt(true);
            txtNombreProducto.requestFocus();
            btnAgregar.setText("ACEPTAR");btnNuevo.setText("CANCELAR");
        }
        else if(btnAgregar.getText().equalsIgnoreCase("ACEPTAR")){
           // mensaje("INGRESO ACEPTAR EMPIEZA");
            if(txtCodProducto.getText().isEmpty() || txtNombreProducto.getText().isEmpty() || txtPrecioUnitario.getText().isEmpty()){
                mensaje("No dejar Campos Vacios");
            }else if(!ruta.equals("")){
               // mensaje("ENTRO A GUARDAR");
                mensaje(oProductoDao.agregarNuevoProducto(Integer.parseInt(txtCodProducto.getText()), txtNombreProducto.getText(),cboNombreUniMed.getSelectedIndex(),
                        Double.parseDouble(txtPrecioUnitario.getText()),cboNombreCategoria.getSelectedIndex(), "No", ruta));
                agregarEvento("Insertar");
                ListarProd();
              limpiar();
              lblImagenProducto.setIcon(null);
            }btnAgregar.setText("AGREGAR");btnNuevo.setText("NUEVO");
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

