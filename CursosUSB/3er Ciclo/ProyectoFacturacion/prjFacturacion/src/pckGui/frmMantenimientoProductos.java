    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if(btnBuscar.getText().equalsIgnoreCase("BUSCAR")){
            limpiar(); txtCodProducto.setEditable(true);txtCodProducto.requestFocus();
            btnBuscar.setText("ACEPTAR");
            btnNuevo.setText("CANCELAR");
        }
        else if(btnBuscar.getText().equalsIgnoreCase("ACEPTAR")){
        
            if(txtCodProducto.getText().isEmpty()){
                mensaje("Ingresar Codigo");txtCodProducto.requestFocus();
            }else {
                
                Producto oProducto=oProductoDao.buscarunProducto(Integer.parseInt( txtCodProducto.getText()));
                
                if(oProducto==null){
                    mensaje("Codigo no Existe");limpiar();txtCodProducto.requestFocus();
                }else {
                    txtNombreProducto.setText(oProducto.getNombreProducto());
                    txtPrecioUnitario.setText(""+oProducto.getPrecioUnitario());
                    cboNombreCategoria.setSelectedIndex(oProducto.getIdCategoria());
                    cboNombreUniMed.setSelectedIndex(oProducto.getIdUnidadMedidad());
                    Image preview=Toolkit.getDefaultToolkit().getImage(ruta);
                    txtRutaarchivo.setText("");
                    ImageIcon icon=new ImageIcon(oProducto.getImagenProd().getScaledInstance(lblImagenProducto.getWidth(), lblImagenProducto.getHeight(),Image.SCALE_DEFAULT));
                    lblImagenProducto.setIcon(icon);
                    desBlqTxt(false);
                    desBloqBtn(true);
                }
            }btnBuscar.setText("BUSCAR");btnNuevo.setText("NUEVO");
            desBlqTxt(false); txtCodProducto.setEditable(false);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

