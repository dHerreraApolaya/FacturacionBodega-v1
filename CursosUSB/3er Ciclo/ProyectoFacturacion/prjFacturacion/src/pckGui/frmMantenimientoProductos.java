    private void btnBuscarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarArchivoActionPerformed
        // TODO add your handling code here:

        JFileChooser elegirImagen=new JFileChooser();
        elegirImagen.setMultiSelectionEnabled(false);
        int o=elegirImagen.showOpenDialog(this);
        
        if(o== JFileChooser.APPROVE_OPTION){
            ruta=elegirImagen.getSelectedFile().getAbsolutePath();
            
            txtRutaarchivo.setText(ruta);
            Image preview =Toolkit.getDefaultToolkit().getImage(ruta);
            if(preview !=null){
                lblImagenProducto.setText("");
                ImageIcon icon=new ImageIcon(preview.getScaledInstance(lblImagenProducto.getWidth(), lblImagenProducto.getHeight(), Image.SCALE_DEFAULT));
                lblImagenProducto.setIcon(icon);
            }
        }
        
    }//GEN-LAST:event_btnBuscarArchivoActionPerformed

