        void listarUnidadMedida(){
         
            DefaultTableModel mod=new DefaultTableModel();
            String columnas[]={
                "Codigo","Nombre"
            };mod.setColumnIdentifiers(columnas);
            ArrayList<UnidadMedida> lista=oUnidMedidDao.listarUnidadMedida();
            for (UnidadMedida unidadMedida : lista) {
                Object data[]={
                  unidadMedida.getIdUnidadMedidad(),unidadMedida.getNombreMedida()
                };mod.addRow(data);
            }tblListaUnidMedida.setModel(mod);lblFilasUnidadMed.setText("Filas Encontradas: "+mod.getRowCount());
        }
