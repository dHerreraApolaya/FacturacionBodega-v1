    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
       if(btnBuscar.getText().equalsIgnoreCase("BUSCAR")){
           limpiar();txtCodigoUnidMedida.setEditable(true);txtCodigoUnidMedida.requestFocus();
           btnBuscar.setText("ACEPTAR");btnNuevo.setText("CANCELAR");
       }
       else if(btnBuscar.getText().equalsIgnoreCase("ACEPTAR"))
                if (txtCodigoUnidMedida.getText().isEmpty()){
                mensaje("Ingrese Codigo a Buscar");txtCodigoUnidMedida.requestFocus();
                }
                else {
                UnidadMedida ounidadmedida=oUnidMedidDao.buscarUnidadMedida(Integer.parseInt(txtCodigoUnidMedida.getText()));
                 if(ounidadmedida==null){
                  mensaje("No existe el Codigo");limpiar(); txtCodigoUnidMedida.requestFocus();
            }
                 else {
                txtNombreUndMedida.setText(ounidadmedida.getNombreMedida());
                txtNombreUndMedida.setEditable(false);desBloqBtn(true);
            }btnBuscar.setText("BUSCAR");btnNuevo.setText("NUEVO");
            txtCodigoUnidMedida.setEditable(false);txtNombreUndMedida.setEditable(false);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
     if(btnAgregar.getText().equalsIgnoreCase("AGREGAR")){
            limpiar();
            txtCodigoUnidMedida.setText(""+(oUnidMedidDao.cantUnidMed()+1));
            txtNombreUndMedida.setEditable(true);
            txtNombreUndMedida.requestFocus();
            btnAgregar.setText("ACEPTAR");btnNuevo.setText("CANCELAR");
        }
        else if(btnAgregar.getText().equalsIgnoreCase("ACEPTAR")){
            if(txtCodigoUnidMedida.getText().isEmpty() || txtNombreUndMedida.getText().isEmpty()){
            mensaje("No dejar Campos Vacios");txtNombreUndMedida.requestFocus();
        }else {
            mensaje(oUnidMedidDao.agregarUnidadMedida(new UnidadMedida(Integer.parseInt(txtCodigoUnidMedida.getText()),txtNombreUndMedida.getText(),"No")));
            agregarEvento("Insertar");
            listarUnidadMedida();
            limpiar();txtCodigoUnidMedida.setText("");
        }btnAgregar.setText("AGREGAR");btnNuevo.setText("NUEVO");
        }
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        // TODO add your handling code here:
             if(btnactualizar.getText().equalsIgnoreCase("ACTUALIZAR")){
           txtNombreUndMedida.setEditable(true);
           txtNombreUndMedida.requestFocus();
           
           btnactualizar.setText("ACEPTAR");btnNuevo.setText("CANCELAR");
       }else if(btnactualizar.getText().equalsIgnoreCase("ACEPTAR")){
            if(txtCodigoUnidMedida.getText().isEmpty() || txtNombreUndMedida.getText().isEmpty()){
            mensaje("No dejar Campos Vacios");txtNombreUndMedida.requestFocus();
        }else {
            mensaje(oUnidMedidDao.actualizarUnidadMedida(new UnidadMedida(Integer.parseInt(txtCodigoUnidMedida.getText()), txtNombreUndMedida.getText(), "No")));
            agregarEvento("Modificar");
            listarUnidadMedida();
            desBloqBtn(false);
            limpiar();
        }
            btnactualizar.setText("ACTUALIZAR");btnNuevo.setText("NUEVO");
       }
        
    }//GEN-LAST:event_btnactualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
         if(txtCodigoUnidMedida.getText().isEmpty()){
            mensaje("Ingresar Codigo");limpiar();
            txtCodigoUnidMedida.setEditable(true);
            txtCodigoUnidMedida.requestFocus();
            }else {
            int x=Integer.parseInt(""+JOptionPane.showConfirmDialog(this, "Seguro de Eliminar la CATEGORIA "+txtNombreUndMedida.getText()+" ????"));
            if(x==0){
            mensaje(oUnidMedidDao.eliminarUnidadMedida(Integer.parseInt(txtCodigoUnidMedida.getText())));
            agregarEvento("Eliminar");
            listarUnidadMedida();
            
            }limpiar();desBloqBtn(false);txtNombreUndMedida.setEditable(false);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

