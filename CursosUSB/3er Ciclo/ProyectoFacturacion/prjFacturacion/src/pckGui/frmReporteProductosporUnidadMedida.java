    void cargarUnidadMedida(){
        classUnidadMedida oUnidadMedidaDao=new classUnidadMedida();
        ArrayList<UnidadMedida> lista=oUnidadMedidaDao.listarUnidadMedida();
        for (UnidadMedida ounidadMedida : lista) {
            cboUnidadMedida.addItem(ounidadMedida.getNombreMedida());
        }
    }
    void listarProductosporUnidadMedida(){
        DefaultTableModel mod=new DefaultTableModel();
        String columnas[]={
          "Codigo","Producto","Unidad Medida","Precio Unitario","Categoria"  
        };mod.setColumnIdentifiers(columnas);
        ArrayList<ProductoConsulta> lista=oProductoDao.listarProductosDetallado();
        for (ProductoConsulta oproductoConsulta : lista) {
            Object data[]={
                oproductoConsulta.getIdProducto(),oproductoConsulta.getNombreProd(),oproductoConsulta.getNombreUnidadMedida(),oproductoConsulta.getPrecioUnitario(),oproductoConsulta.getNombreCategoria()
            };mod.addRow(data);
        }tblProdporCategoria.setModel(mod);lblFilas.setText("Filas Encontradas: "+tblProdporCategoria.getRowCount());
    }
    void listarProductosporUnidadMedida(int codUnidadMedida){
        DefaultTableModel mod=new DefaultTableModel();
        String columnas[]={
          "Codigo","Producto","Unidad Medida","Precio Unitario","Categoria"  
        };mod.setColumnIdentifiers(columnas);
        ArrayList<ProductoConsulta> lista=oProductoDao.listarProductosporUnidadMedida(codUnidadMedida);
        for (ProductoConsulta oproductoConsulta : lista) {
            Object data[]={
                oproductoConsulta.getIdProducto(),oproductoConsulta.getNombreProd(),oproductoConsulta.getNombreUnidadMedida(),oproductoConsulta.getPrecioUnitario(),oproductoConsulta.getNombreCategoria()
            };mod.addRow(data);
        }tblProdporCategoria.setModel(mod);lblFilas.setText("Filas Encontradas: "+tblProdporCategoria.getRowCount());
        
    }
    void consultar(){
        int x=cboUnidadMedida.getSelectedIndex();
        if(x==0){
            listarProductosporUnidadMedida();
        }else if(x>0){
            listarProductosporUnidadMedida(x);
        }
    }
    
    
