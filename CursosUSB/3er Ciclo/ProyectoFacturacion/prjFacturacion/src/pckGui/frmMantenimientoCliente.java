    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
       if(btnAgregar.getText().equalsIgnoreCase("AGREGAR")){
         limpiar();
         desBloqTxt(true);
         txtCodigo.setEditable(true);
         txtCodigo.requestFocus();
           btnAgregar.setText("ACEPTAR");btnNuevo.setText("CANCELAR");
       }else if(btnAgregar.getText().equalsIgnoreCase("ACEPTAR")){
           if (txtCodigo.getText().isEmpty() || txtNombre.getText().isEmpty()){
            mensaje("Llenar Campos Vacios");
        }
           else {
            mensaje(oclientedao.agregarCliente(new Cliente(txtCodigo.getText(),txtNombre.getText(),
                    txtDireccion.getText(),txtTelefono.getText(),txtxCorreo.getText(),"No")));
           // dise√±otabla();
           agregarEvento("Insertar");
            listarCliente();
            limpiar();
        }btnAgregar.setText("AGREGAR");btnNuevo.setText("NUEVO");
       }
        
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        if(btnActualizar.getText().equalsIgnoreCase("ACTUALIZAR")){
            desBloqTxt(true);txtNombre.requestFocus();
            btnActualizar.setText("ACEPTAR");btnNuevo.setText("CANCELAR");
        }
        else if(btnActualizar.getText().equalsIgnoreCase("ACEPTAR")){
            if(txtCodigo.getText().isEmpty() || txtNombre.getText().isEmpty()){
            mensaje("Completar Campos Vacios");txtTelefono.requestFocus();
        }else {
            Cliente uncliente=new Cliente(txtCodigo.getText(),txtNombre.getText(), txtDireccion.getText(), txtTelefono.getText(),txtxCorreo.getText(),"No");
            mensaje(oclientedao.actualizarCliente(uncliente));
            agregarEvento("Modificar");
            listarCliente();
            desBloqBtn(false);
            limpiar();
        }btnActualizar.setText("ACTUALIZAR");btnNuevo.setText("NUEVO");
        }
        
        
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if(txtCodigo.getText().isEmpty()){
            mensaje("Ingrese Codigo");limpiar();
            txtCodigo.setEditable(true);txtCodigo.requestFocus();
        }else {
            int x=Integer.parseInt(""+JOptionPane.showConfirmDialog(this, "Seguro de Eliminar el Cliente "+txtNombre.getText()+" ??"));
            if(x==0){
                mensaje(oclientedao.eliminarCliente(txtCodigo.getText()));
                agregarEvento("Eliminar");
                listarCliente();
            }
            limpiar();desBloqBtn(false);desBloqTxt(false);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
      if(btnBuscar.getText().equalsIgnoreCase("BUSCAR")){
          limpiar();txtCodigo.setEditable(true);txtCodigo.requestFocus();
          btnBuscar.setText("ACEPTAR");btnNuevo.setText("CANCELAR");
      }
      else if(btnBuscar.getText().equalsIgnoreCase("ACEPTAR")){
          if(txtCodigo.getText().isEmpty()){
            mensaje("Ingrese Codigo");txtCodigo.requestFocus();
        }else {
            Cliente uncliente=oclientedao.buscarCliente(txtCodigo.getText());
            if(uncliente==null){
                mensaje("Cliente No EXISTE");limpiar();txtCodigo.requestFocus();
            }else {
                txtDireccion.setText(uncliente.getDireccion());
                txtNombre.setText(uncliente.getNombreCliente());
                txtTelefono.setText(uncliente.getTelefono());
                txtxCorreo.setText(uncliente.getCorreo());
                desBloqBtn(true);desBloqTxt(false);
            }
        }btnBuscar.setText("BUSCAR");btnNuevo.setText("NUEVO");
          desBloqTxt(false); txtCodigo.setEditable(false);
      }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

