    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if(btnAgregar.getText().equalsIgnoreCase("AGREGAR")){
            limpiar();
            txtidOpcionPago.setText(""+(oTipoPagoDao.codigoTipoPago()+1));
            txtnombreOpcionPago.setEditable(true);
            txtnombreOpcionPago.requestFocus();
            btnAgregar.setText("ACEPTAR");btnNuevo.setText("CANCELAR");
        }
        else if(btnAgregar.getText().equalsIgnoreCase("ACEPTAR")){
            if(txtidOpcionPago.getText().isEmpty() || txtnombreOpcionPago.getText().isEmpty()){
            mensaje("No dejar Campos Vacios");
        }else {
            mensaje(oTipoPagoDao.agregarTipoPago(new TipoPago(Integer.parseInt(txtidOpcionPago.getText()), txtnombreOpcionPago.getText(),"No")));
            agregarEvento("Insertar");
            listarOpcionPago();
            limpiar();
        }btnAgregar.setText("AGREGAR");btnNuevo.setText("NUEVO");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if(btnBuscar.getText().equalsIgnoreCase("BUSCAR")){
            limpiar(); txtidOpcionPago.setEditable(true);txtidOpcionPago.requestFocus();
            btnBuscar.setText("ACEPTAR");
            btnNuevo.setText("CANCELAR");
        }
        else if(btnBuscar.getText().equalsIgnoreCase("ACEPTAR")){
              if(txtidOpcionPago.getText().isEmpty()){
            mensaje("Ingresar Codigo");txtidOpcionPago.requestFocus();
        }else {
            TipoPago oTipoPago=oTipoPagoDao.buscarTipoPago(Integer.parseInt(txtidOpcionPago.getText()));
            if(oTipoPago==null){
            mensaje("Codigo no Existe");limpiar();txtidOpcionPago.requestFocus();
            }else {
                txtnombreOpcionPago.setText(oTipoPago.getNombreOpcionPago());
                txtnombreOpcionPago.setEditable(false); 
                desBLoqBtn(true);
            }
        }btnBuscar.setText("BUSCAR");btnNuevo.setText("NUEVO");
            txtnombreOpcionPago.setEditable(false); txtidOpcionPago.setEditable(false);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
         if(btnActualizar.getText().equalsIgnoreCase("ACTUALIZAR")){
           txtnombreOpcionPago.setEditable(true);
           txtnombreOpcionPago.requestFocus();
           
           btnActualizar.setText("ACEPTAR");btnNuevo.setText("CANCELAR");
       }else if(btnActualizar.getText().equalsIgnoreCase("ACEPTAR")){
            if(txtidOpcionPago.getText().isEmpty() || txtnombreOpcionPago.getText().isEmpty()){
            mensaje("No dejar Campos Vacios");txtnombreOpcionPago.requestFocus();
        }else {
                mensaje(oTipoPagoDao.actualizarTipoPago(new TipoPago(Integer.parseInt(txtidOpcionPago.getText()),txtnombreOpcionPago.getText(), "No")));
               agregarEvento("Modificar");
                listarOpcionPago();
                desBLoqBtn(false);
            limpiar();
        }
            btnActualizar.setText("ACTUALIZAR");btnNuevo.setText("NUEVO");
       }
        
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if(txtidOpcionPago.getText().isEmpty()){
            mensaje("Ingresar Codigo");limpiar();
            txtidOpcionPago.setEditable(true);
            txtidOpcionPago.requestFocus();
            }else {
            int x=Integer.parseInt(""+JOptionPane.showConfirmDialog(this, "Seguro de Eliminar la Opcion Pago "+txtnombreOpcionPago.getText()+" ????"));
            if(x==0){
                mensaje(oTipoPagoDao.eliminadoLogicoTipoPago(Integer.parseInt(txtidOpcionPago.getText())));
               agregarEvento("Eliminar");
                listarOpcionPago();
            
            }limpiar();desBLoqBtn(false);txtnombreOpcionPago.setEditable(false);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

