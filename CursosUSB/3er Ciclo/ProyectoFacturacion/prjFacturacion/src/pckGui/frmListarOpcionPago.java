    void listarOpcionPago(){
        DefaultTableModel mod=new DefaultTableModel();
        String []columnas={"Codigo","Nombre Opcion Pago"};
        mod.setColumnIdentifiers(columnas);
        
        ArrayList<TipoPago> lista=oTipoPagoDao.listarTipoPago();
        for (TipoPago otipoPago : lista) {
            Object data[]={otipoPago.getIdOpcionPago(),otipoPago.getNombreOpcionPago()};
        mod.addRow(data);
        }  tblListaOpcionPago.setModel(mod); lblFilas.setText(""+tblListaOpcionPago.getRowCount());
    }
    
