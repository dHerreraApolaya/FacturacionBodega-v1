    void listarProductos(){
        DefaultTableModel mod=new DefaultTableModel();
        String []columnas={
          "Codigo","Producto","Unidad Medida","Precio Unitario","Categoria"  
        };mod.setColumnIdentifiers(columnas);
        tblProdporCategoria.setModel(new DefaultTableModel());
        
        ArrayList<ProductoConsulta> lista=oProductoDao.listarProductosDetallado();
        for (ProductoConsulta oproductoConsulta : lista) {
            Object data[]={
              oproductoConsulta.getIdProducto(),oproductoConsulta.getNombreProd(),oproductoConsulta.getNombreUnidadMedida(),oproductoConsulta.getPrecioUnitario(),oproductoConsulta.getNombreCategoria()
            };mod.addRow(data);
        }tblProdporCategoria.setModel(mod);lblfilas.setText("Filas Encontradas: "+tblProdporCategoria.getRowCount());
    }
    
    void listarProductoporCategoria(int idCategoria){
       DefaultTableModel mod=new DefaultTableModel();
        String []columnas={
          "Codigo","Producto","Unidad Medida","Precio Unitario","Categoria"  
        };mod.setColumnIdentifiers(columnas);
        
        ArrayList<ProductoConsulta> lista=oProductoDao.listarProductosporCategoria(idCategoria);
        for (ProductoConsulta oproductoConsulta : lista) {
            Object data[]={
              oproductoConsulta.getIdProducto(),oproductoConsulta.getNombreProd(),oproductoConsulta.getNombreUnidadMedida(),oproductoConsulta.getPrecioUnitario(),oproductoConsulta.getNombreCategoria()
            };mod.addRow(data);
        }tblProdporCategoria.setModel(mod);lblfilas.setText("Filas Encontradas: "+tblProdporCategoria.getRowCount());
    }
    
    void cargarNombreCategoria(){
        classCategoriaDao oCategoriaDao=new classCategoriaDao();
        ArrayList<Categoria> lista=oCategoriaDao.listarCategoria();
        for (Categoria ocategoria : lista) {
            cboCategoria.addItem(ocategoria.getNombreCategoria());
        }
    }
    void consulta(){
         int x=cboCategoria.getSelectedIndex();
        
        if(x==0){
            listarProductos();
        }else if(x>0){
            listarProductoporCategoria(x);
        }
    }
